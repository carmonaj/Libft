int		ft_is_prime(int nb)
{
	int count;
	int nbr1;

	count = 0;
	nbr1 = 1;
	while (nbr1 <= nb)
	{
		if (nb % nbr1 == 0)
			count++;
		nbr1++;
	}
	if (count == 2)
		return (1);
	else
		return (0);
}
