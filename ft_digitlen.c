int	ft_digitlen(int n)
{
	int i;

	i = 0;
	if (n == -2147483648)
		return (11);
	if (n <= 0)
	{
		n *= -1;
		i++;
	}
	while (n > 0)
	{
		n /= 10;
		i++;
	}
	return (i);
}
